package com.DSA2;

import java.util.Queue;
import java.util.Stack;

public class StackAndQueue {
    public static void main(String[] args) {
        Stack<Integer> stack = new Stack<>();
        stack.push(33);
        stack.push(53);
        stack.push(77);
        stack.push(98);

        System.out.println(stack.pop());

    }

}
