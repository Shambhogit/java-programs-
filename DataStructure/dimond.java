package com.DataStructure;

public class dimond {
    public static void main(String[] args){
        for(int i = 0;i<3;i++){
            for(int k = 2;k>0;k--){
                System.out.print(" ");
            }
            for(int j = 0;j<i+4;j++){
                System.out.print("* ");
            }
            System.out.println();
        }

    }
}
